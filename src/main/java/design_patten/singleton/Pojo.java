package design_patten.singleton;

/**
 * @Author: LX
 * @Date: 2019/4/1 20:10
 * @Version: 1.0
 */
public class Pojo {
    int age;
    String name;

    public Pojo() {
    }

    public int getAge() {
        return age;
    }

    public void setAge(int age) {
        this.age = age;
    }

    public String getName() {
        return name;
    }

    public void setName(String name) {
        this.name = name;
    }
}
